package Clase10;

public class Operaciones {
    public static int sumar(int a, int b) {
        System.out.println("Metodo para sumar dos enteros");
        return a + b;
    }
    public static double sumar(double a, double b) {
        System.out.println("Metodo para sumar dos doubles");
        return a + b;
    }
}
